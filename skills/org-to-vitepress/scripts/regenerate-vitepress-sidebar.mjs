#!/usr/bin/env node
/**
 * Scan <root>/<srcDir>/**\/*.md and rewrite
 *   <root>/.vitepress/sidebar.generated.mts
 *
 * Sidebar is keyed by top-level subdirectory under srcDir. Nested subdirectories
 * become nested `items` groups. Leaf item `text` prefers frontmatter `title`,
 * falling back to the filename (minus .md).
 *
 * Usage:
 *   node regenerate-vitepress-sidebar.mjs --root <repo-root> [--src-dir org-wiki]
 */

import fs from "node:fs";
import path from "node:path";

function parseArgs(argv) {
  const out = { root: process.cwd(), srcDir: "org-wiki" };
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--root" && argv[i + 1]) out.root = path.resolve(argv[++i]);
    else if (a === "--src-dir" && argv[i + 1]) out.srcDir = argv[++i].replace(/^\.?\/+|\/+$/g, "");
  }
  return out;
}

function readFrontmatterTitle(filePath) {
  try {
    const raw = fs.readFileSync(filePath, "utf8");
    const m = raw.match(/^---\r?\n([\s\S]*?)\r?\n---/);
    if (!m) return null;
    const titleMatch = m[1].match(/^title:\s*(.+?)\s*$/m);
    if (!titleMatch) return null;
    return titleMatch[1].trim().replace(/^["']|["']$/g, "");
  } catch {
    return null;
  }
}

/** Capitalize dash-separated names for display: "system-design" → "System Design". */
function prettifyName(name) {
  return name
    .split(/[-_]/)
    .map((w) => (w ? w.charAt(0).toUpperCase() + w.slice(1) : w))
    .join(" ");
}

/**
 * Recursively build a sidebar subtree for a directory.
 * Returns an array of VitePress sidebar item objects:
 *   { text, link }                   (leaf file)
 *   { text, collapsed, items: [...] } (nested group)
 */
function buildSubtree(absDir, linkBase) {
  let entries;
  try {
    entries = fs.readdirSync(absDir, { withFileTypes: true });
  } catch {
    return [];
  }

  /** @type {Array<{ text: string; link: string }>} */
  const fileItems = [];
  /** @type {Array<{ text: string; collapsed: true; items: any[] }>} */
  const dirItems = [];

  for (const entry of entries) {
    if (entry.name.startsWith(".")) continue;
    if (entry.name === "INDEX.md") continue;
    const abs = path.join(absDir, entry.name);
    if (entry.isDirectory()) {
      const nested = buildSubtree(abs, `${linkBase}/${entry.name}`);
      if (nested.length > 0) {
        dirItems.push({
          text: prettifyName(entry.name),
          collapsed: true,
          items: nested,
        });
      }
    } else if (entry.isFile() && entry.name.toLowerCase().endsWith(".md")) {
      const base = entry.name.replace(/\.md$/i, "");
      const title = readFrontmatterTitle(abs) || base;
      fileItems.push({ text: title, link: `${linkBase}/${base}` });
    }
  }

  fileItems.sort((a, b) => a.text.localeCompare(b.text));
  dirItems.sort((a, b) => a.text.localeCompare(b.text));

  return [...fileItems, ...dirItems];
}

function main() {
  const { root, srcDir } = parseArgs(process.argv);
  const wikiRoot = path.join(root, srcDir);
  const vpDir = path.join(root, ".vitepress");
  const outFile = path.join(vpDir, "sidebar.generated.mts");

  if (!fs.existsSync(wikiRoot)) {
    console.error(`missing srcDir: ${wikiRoot}`);
    process.exit(1);
  }
  if (!fs.existsSync(vpDir)) {
    console.error(`missing .vitepress directory: ${vpDir}`);
    process.exit(1);
  }

  const topEntries = fs
    .readdirSync(wikiRoot, { withFileTypes: true })
    .filter((e) => e.isDirectory() && !e.name.startsWith("."))
    .map((e) => e.name)
    .sort();

  /** Keyed sidebar: VitePress can render different sidebars per path prefix. */
  /** @type {Record<string, any[]>} */
  const sidebar = {};

  /** Top-level nav: link to the first leaf of each section (or section root). */
  /** @type {Array<{ text: string; link: string }>} */
  const nav = [{ text: "首页", link: "/INDEX" }];

  for (const dir of topEntries) {
    const abs = path.join(wikiRoot, dir);
    const items = buildSubtree(abs, `/${dir}`);
    if (items.length === 0) continue;

    const label = prettifyName(dir);
    sidebar[`/${dir}/`] = [{ text: label, items }];

    // Pick first leaf for the nav entry (walk until we find one with a `link`)
    let firstLink = null;
    const stack = [...items];
    while (stack.length) {
      const it = stack.shift();
      if (it.link) {
        firstLink = it.link;
        break;
      }
      if (Array.isArray(it.items)) stack.unshift(...it.items);
    }
    nav.push({ text: label, link: firstLink || `/${dir}/` });
  }

  const banner =
    "// Auto-generated by skills/org-to-vitepress/scripts/regenerate-vitepress-sidebar.mjs\n" +
    "// Do not edit by hand; re-run the script after adding or renaming docs.\n\n" +
    "// @ts-nocheck\n\n";

  const body =
    `export const wikiSidebar = ${JSON.stringify(sidebar, null, 2)};\n\n` +
    `export const wikiNav = ${JSON.stringify(nav, null, 2)};\n`;

  fs.writeFileSync(outFile, banner + body, "utf8");
  console.log(`wrote ${path.relative(root, outFile)}`);
  console.log(`sections: ${Object.keys(sidebar).length}, nav entries: ${nav.length}`);
}

main();
